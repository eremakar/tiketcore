        protected override async Task DidUpdateAsyncCore(Railway source, Railway original)
        {
            var stations = await db.RailwayStations.
                Where(_ => _.RailwayId == original.Id).ToListAsync();

            db.RailwayStations.RemoveRange(stations);
            db.RailwayStations.AddRange(source.Stations);
        }

